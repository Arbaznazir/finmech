export function getCatalogEntry(modelSlug) {
  return MODEL_HINT_CATALOG[modelSlug] ?? null;
}

export function listModelsByTier(tier) {
  return Object.entries(MODEL_HINT_CATALOG)
    .filter(([, meta]) => meta.tier === tier)
    .map(([slug, meta]) => ({
      modelSlug: slug,
      name: meta.name,
      fieldCount: meta.fields.length,
    }));
}

export const TIER_OVERVIEW_SLUG = 'tier-overview';

export async function mergeTierOverviewHints(prisma) {
  return mergeHintsForModel(prisma, TIER_OVERVIEW_SLUG);
}

export async function mergeHintsForModel(prisma, modelSlug) {
  const catalog = getCatalogEntry(modelSlug);
  if (!catalog || catalog.fields.length === 0) return null;

  const overrides = await prisma.modelFieldHint.findMany({ where: { modelSlug } });
  const map = new Map(overrides.map((o) => [o.fieldKey, o]));

  const fields = catalog.fields.map((def) => {
    const o = map.get(def.key);
    return {
      fieldKey: def.key,
      label: def.label,
      section: def.section,
      what: o?.what ?? def.what,
      why: o?.why ?? def.why,
      how: o?.how ?? def.how ?? null,
      isCustom: Boolean(o),
      defaultWhat: def.what,
      defaultWhy: def.why,
      defaultHow: def.how ?? null,
    };
  });

  const hints = Object.fromEntries(
    fields.map((f) => [f.fieldKey, { what: f.what, why: f.why, ...(f.how ? { how: f.how } : {}) }])
  );

  return {
    modelSlug,
    tier: catalog.tier,
    name: catalog.name,
    fields,
    hints,
  };
}
